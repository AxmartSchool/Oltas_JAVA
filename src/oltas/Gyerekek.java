    private void insertTable() {
        
        int sorokSzama= model.getRowCount();
        for (int i = sorokSzama; i > 0; i--) {
            model.removeRow(i-1);
        }
        
        for (Gyerek gyerek : GyerekList) {
            
            model.addRow(new Object[]{gyerek.getId(),gyerek.getNev(),gyerek.getApja(),gyerek.getAnyja(),gyerek.getTajszam(),gyerek.getSzuletett()});
            
        }
        
        
    }
