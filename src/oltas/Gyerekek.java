    private void jTbl_gyerekAdatokMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTbl_gyerekAdatokMouseClicked
        // TODO add your handling code here:
        int sorIndex = jTbl_gyerekAdatok.getSelectedRow();
        jTxt_Azonosito.setText(String.valueOf( GyerekList.get(sorIndex).getId()));
        jTxt_Nev.setText(GyerekList.get(sorIndex).getNev());
        jTxt_ApjaNeve.setText(GyerekList.get(sorIndex).getApja());
        jTxt_anyjaNeve.setText(GyerekList.get(sorIndex).getAnyja());
        
        ArrayList<Oltas> oltasok = Oltas.Beolvasas(GyerekList.get(sorIndex).getId());
        
        String oltasokFelsorolas = "";
        int osszeg = 0;
        int darab = 0;
        
        for (Oltas oltas : oltasok) {
            
            oltasokFelsorolas+= oltas.getElnevezes()+"\n";
            osszeg += oltas.getFizetendo();
            darab++;
        }
        
        jTxta_oltasok.setText(oltasokFelsorolas); 
        jTxt_fizetettOsszeg.setText(String.valueOf(osszeg));
        jTxt_oltasokDarabszama.setText(String.valueOf(darab));
        
        
    }//GEN-LAST:event_jTbl_gyerekAdatokMouseClicked

    private void jBtn_modositasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_modositasActionPerformed
        // TODO add your handling code here:
        jlbl_hiba.setText("");
        if (jTxt_Nev.getText().trim().length() < 1 || jTxt_ApjaNeve.getText().trim().length() < 1 || jTxt_anyjaNeve.getText().trim().length() < 1) {
            
            jlbl_hiba.setText("egyik mezo sem lehet ures");
        }else   
        if (jTxt_Nev.getText().trim().length() > 30 || jTxt_ApjaNeve.getText().trim().length() > 30 || jTxt_anyjaNeve.getText().trim().length() > 30) {
            jlbl_hiba.setText("egyik mezo sem lehet tobb 30 karakternel");
        }else{
            Gyerek modositando = new Gyerek(jTbl_gyerekAdatok.getSelectedRow()+1, jTxt_Nev.getText(), jTxt_ApjaNeve.getText(), jTxt_anyjaNeve.getText(), "", null);
            Gyerek.Modositas(modositando);
            GyerekList = Gyerek.Beolvasas();
            insertTable();
            
        }
        
        
        
        
        
    }//GEN-LAST:event_jBtn_modositasActionPerformed

